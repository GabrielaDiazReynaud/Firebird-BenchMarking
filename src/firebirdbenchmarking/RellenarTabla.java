    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        DefaultTableModel n = (DefaultTableModel) jTable1.getModel();
        jProgressBar1.setStringPainted(true);
        jProgressBar1.setValue(0);
        jTextField1.setText("Iniciando Proceso............");
        jTextField1.update(jTextField1.getGraphics());
        jProgressBar1.update(jProgressBar1.getGraphics());
        int size = tables.size();
        int percentage = 100 / size;
        int total = 0;
        for (int x = 0; x < tables.size(); x++) {
            ArrayList<String> types = new ArrayList<String>();
             ArrayList<Integer> length= new ArrayList<Integer>();
            Object obj = n.getValueAt(x, 0);
            Boolean selected = Boolean.valueOf(obj.toString());
            if (selected == true) {
                obj = n.getValueAt(x, 1);
                String tname = obj.toString().trim();
                obj = n.getValueAt(x, 2);
                types= FirebirdBenchMarking.conexion.types(tname);
                length=FirebirdBenchMarking.conexion.fieldlength(tname);
                int nregistro = Integer.parseInt(obj.toString());
                obj = n.getValueAt(x, 3);
                Boolean delete = Boolean.valueOf(obj.toString());
                System.out.println(tname + " " + nregistro + " " + delete);
                
                for(int num=0; num< nregistro;num++){
                    FirebirdBenchMarking.conexion.llenarTabla(types, length, tname);
                }
                
                
                total += percentage;

                try {
                    TimeUnit.SECONDS.sleep(1);
                } catch (InterruptedException ex) {
                    Logger.getLogger(RellenarTabla.class.getName()).log(Level.SEVERE, null, ex);
                }
                jTextField1.setText("Llenando " + tname.toLowerCase());
                jTextField1.update(jTextField1.getGraphics());
                jProgressBar1.setValue(total);
                jProgressBar1.update(jProgressBar1.getGraphics());

            }
        }
        try {
            TimeUnit.SECONDS.sleep(1);
        } catch (InterruptedException ex) {
            Logger.getLogger(RellenarTabla.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextField1.setText("Proceso finalizado ");
        jProgressBar1.setValue(100);
    }//GEN-LAST:event_jButton1MouseClicked

